        <h1>Política de Privacidade da Capital Soluções</h1>
        <p><strong>Última atualização: abril de 2025</strong></p>
        <p>A Capital Soluções tem o compromisso de proteger a privacidade dos seus clientes e usuários do site <a href="https://capitalsolucoesam.com.br">capitalsolucoesam.com.br</a>. Esta Política de Privacidade explica como coletamos, usamos, armazenamos e protegemos seus dados pessoais, bem como seus direitos em relação a essas informações.</p>
        
        <h2>1. Coleta de Dados</h2>
        <p>Coletamos informações pessoais quando você acessa nosso site, utiliza nossos serviços ou entra em contato conosco. Os dados podem incluir:</p>
        <ul>
            <li>Nome completo;</li>
            <li>CPF e RG;</li>
            <li>Data de nascimento;</li>
            <li>Endereço residencial e eletrônico (e-mail);</li>
            <li>Número de telefone;</li>
            <li>Dados bancários para análise de crédito;</li>
            <li>Dados de consulta de saldo FGTS;</li>
            <li>Informações financeiras para solicitação de empréstimos.</li>
        </ul>
        
        <h2>2. Uso dos Dados</h2>
        <p>Os dados coletados são utilizados para:</p>
        <ul>
            <li>Consultar saldo FGTS de forma segura e autorizada;</li>
            <li>Avaliação e concessão de empréstimos para pensionistas e demais clientes;</li>
            <li>Personalização de ofertas e serviços financeiros;</li>
            <li>Atendimento ao cliente e suporte;</li>
            <li>Cumprimento de obrigações legais e regulatórias.</li>
        </ul>
        
        <h2>3. Compartilhamento de Dados</h2>
        <p>Podemos compartilhar seus dados com terceiros apenas quando necessário para a prestação dos serviços, cumprimento de obrigações legais ou com seu consentimento. Os destinatários podem incluir:</p>
        <ul>
            <li>Instituições financeiras parceiras;</li>
            <li>Órgãos reguladores e fiscalizadores;</li>
            <li>Provedores de tecnologia e segurança da informação.</li>
        </ul>
        
        <h2>4. Armazenamento e Proteção de Dados</h2>
        <p>Adotamos medidas técnicas e administrativas para garantir a segurança dos dados pessoais, incluindo criptografia, controle de acesso e monitoramento contínuo. Seus dados são armazenados em ambientes seguros e protegidos contra acessos não autorizados.</p>
        
        <h2>5. Seus Direitos</h2>
        <p>Você tem direito a:</p>
        <ul>
            <li>Acessar, corrigir ou excluir seus dados pessoais;</li>
            <li>Revogar seu consentimento a qualquer momento;</li>
            <li>Solicitar informações sobre o tratamento dos seus dados;</li>
            <li>Portabilidade dos seus dados, conforme a legislação vigente.</li>
        </ul>
        <p>Para exercer seus direitos, entre em contato pelo e-mail: <a href="mailto:contato@capitalsolucoesam.com.br">contato@capitalsolucoesam.com.br</a>.</p>
        
        <h2>6. Cookies e Tecnologias de Rastreamento</h2>
        <p>Utilizamos cookies para melhorar sua experiência em nosso site. Você pode gerenciar as configurações de cookies no seu navegador a qualquer momento.</p>
        
        <h2>7. Alterações nesta Política</h2>
        <p>Podemos atualizar esta Política de Privacidade periodicamente. Notificaremos os usuários sobre mudanças relevantes por meio do site ou outros canais de comunicação.</p>
        
